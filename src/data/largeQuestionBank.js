function rotate(items, amount) {
  const offset = amount % items.length;
  return [...items.slice(offset), ...items.slice(0, offset)];
}

export const largeQuestionBank = [];

let nextId = 301;
const conceptsByCategory = concepts.reduce((groups, concept) => {
  const category = concept[0];
  groups.set(category, [...(groups.get(category) ?? []), concept]);
  return groups;
}, new Map());
const categoryOrder = [...conceptsByCategory.keys()];

for (let templateIndex = 0; templateIndex < templates.length && nextId <= 1000; templateIndex += 1) {
  const template = templates[templateIndex];
  let conceptIndex = 0;
  let hasRemainingConcept = true;

  while (hasRemainingConcept && nextId <= 1000) {
    hasRemainingConcept = false;

    for (const category of categoryOrder) {
      const categoryConcepts = conceptsByCategory.get(category) ?? [];
      const concept = categoryConcepts[conceptIndex];
      if (!concept) continue;

      hasRemainingConcept = true;
      const [, subject, answer, wrongs, explanation] = concept;
      const choices = [answer, ...rotate(wrongs, nextId).slice(0, 3)];
      largeQuestionBank.push({
        id: nextId,
        category,
        question: template({ subject }),
        choices,
        answer: 0,
        explanation,
      });
      nextId += 1;

      if (nextId > 1000) break;
    }

    conceptIndex += 1;
  }
}
