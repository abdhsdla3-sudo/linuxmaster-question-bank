const TARGET_QUESTION_COUNT = 1000;

function tagQuestions(items, difficulty) {
  return items.map((question) => ({ ...question, difficulty }));
}

const fixedQuestions = [
  ...tagQuestions(baseQuestions, "기초"),
  ...tagQuestions(extraQuestions, "기초"),
  ...tagQuestions(moreQuestions, "중간"),
  ...tagQuestions(hardQuestions, "어려움"),
  ...tagQuestions(trickyQuestions, "헷갈림"),
  ...tagQuestions(practicalQuestions, "실전"),
  ...tagQuestions(coverageQuestions, "실전"),
];
const generatedQuestionSlots = TARGET_QUESTION_COUNT - fixedQuestions.length;
const rawQuestions = [
  ...fixedQuestions,
  ...tagQuestions(largeQuestionBank.slice(0, generatedQuestionSlots), "중간"),
];

function seededShuffle(items, seed) {
  const result = [...items];
  let value = seed * 9301 + 49297;
  for (let index = result.length - 1; index > 0; index -= 1) {
    value = (value * 9301 + 49297) % 233280;
    const swapIndex = value % (index + 1);
    [result[index], result[swapIndex]] = [result[swapIndex], result[index]];
  }
  return result;
}

// 보기 순서가 항상 정답 1번으로 고정되지 않도록 id 기반으로 안정적으로 섞습니다.
export const questions = rawQuestions.map((question) => {
  const correctChoice = question.choices[question.answer];
  const mixedChoices = seededShuffle(question.choices, question.id);
  return {
    ...question,
    question: makeReadableQuestion(question),
    choices: mixedChoices,
    answer: mixedChoices.indexOf(correctChoice),
  };
});

export const categories = [...new Set(questions.map((question) => question.category))];
